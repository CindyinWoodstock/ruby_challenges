weather = 'rainy'

case weather

when 'rainy' 
	puts "Don't forget your umbrella!"
when 'sunny'
	puts "You'll need your sunglasses today!"
when 'snowy'
	puts "Better bundle up, it's cold outside!"
when 'windy'
	puts "Hold onto your hat, it's quite windy today!"
when 'icy'
	puts "Dangerous driving condtions today, best to stay home if you can!"
when 'humid'
	puts "It's so humid today, best to pull your hair back to save stress!"
else 
	puts "You're on your own in THIS kind of weather!"
	
end